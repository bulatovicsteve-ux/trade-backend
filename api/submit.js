/**
 * Main handler
 */
export default async function handler(req, res) {
  // CORS headers
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') {
    return res.status(200).end();
  }

  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    // Parse form data with files
    const form = formidable({
      maxFileSize: 50 * 1024 * 1024, // 50MB total
      maxFiles: 20,
    });

    const [fields, files] = await new Promise((resolve, reject) => {
      form.parse(req, (err, fields, files) => {
        if (err) reject(err);
        resolve([fields, files]);
      });
    });

    // Extract form data
    const formData = {};
    Object.keys(fields).forEach(key => {
      formData[key] = fields[key]?.[0] || '';
    });

    const uploadedFiles = files.files || [];
    const fileArray = Array.isArray(uploadedFiles) ? uploadedFiles : [uploadedFiles];

    // Prepare attachments for email
    const attachments = fileArray.map(file => ({
      content: fs.readFileSync(file.filepath).toString('base64'),
      filename: file.originalFilename,
      type: file.mimetype,
      disposition: 'attachment'
    }));

    // Email to Trades Desk
    const tradeDeskEmail = {
      to: process.env.TRADES_DESK_EMAIL || formData.tradesDeskEmail || 'trades@suttonstore.com',
      from: {
        email: 'sbulatovic@sutton.com',
        name: 'Trade Record Generator'
      },
      subject: `New ${formData.transactionType === 'seller' ? 'Seller' : 'Buyer'} Trade: ${formData.propertyAddress || 'No Address'}`,
      html: generateEmailHTML({
        ...formData,
        fileCount: fileArray.length
      }),
      attachments: attachments
    };

    // Add CC if provided
    if (formData.ccEmails) {
      const ccList = formData.ccEmails.split(',').map(email => email.trim()).filter(Boolean);
      if (ccList.length > 0) {
        tradeDeskEmail.cc = ccList;
      }
    }

    // Confirmation email to agent
    const confirmationEmail = {
      to: formData.agentEmail,
      from: {
        email: 'sbulatovic@sutton.com',
        name: 'Trade Record Generator'
      },
      subject: '‚úÖ Trade Submission Confirmation',
      html: generateConfirmationHTML({
        ...formData,
        fileCount: fileArray.length
      })
    };

    // Send both emails
    await Promise.all([
      sgMail.send(tradeDeskEmail),
      sgMail.send(confirmationEmail)
    ]);

    // Clean up temp files
    fileArray.forEach(file => {
      try {
        fs.unlinkSync(file.filepath);
      } catch (e) {
        console.error('Failed to delete temp file:', e);
      }
    });

    return res.status(200).json({
      success: true,
      message: 'Trade submitted successfully',
      filesSubmitted: fileArray.length
    });

  } catch (error) {
    console.error('Submission error:', error);
    return res.status(500).json({
      success: false,
      error: 'Failed to submit trade',
      message: error.message
    });
  }
}
